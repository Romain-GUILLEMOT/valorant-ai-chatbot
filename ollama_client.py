def load_env(path=".env"):
    if not os.path.exists(path):
        return

    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith("#") or "=" not in line:
                continue

            key, value = line.split("=", 1)
            os.environ.setdefault(key.strip(), value.strip().strip('"').strip("'"))


def ollama_settings():
    load_env()
    return {
        "url": os.environ.get("OLLAMA_URL", DEFAULT_OLLAMA_URL).rstrip("/"),
        "model": os.environ.get("OLLAMA_MODEL", DEFAULT_OLLAMA_MODEL),
        "timeout": float(os.environ.get("OLLAMA_TIMEOUT", "8")),
        "temperature": float(os.environ.get("OLLAMA_TEMPERATURE", "0.9")),
        "keep_alive": os.environ.get("OLLAMA_KEEP_ALIVE", "45m"),
        "player_name": os.environ.get("PLAYER_NAME", "Yotakipa")
    }


