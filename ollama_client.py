def compact_player(row, player_name):
    name = row.get("name") or ""
    return {
        "team": row.get("team"),
        "slot": row.get("slot"),
        "name": row.get("name"),
        "is_me": name.lower() == player_name.lower(),
        "agent": row.get("agent"),
        "rank": row.get("rank"),
        "level": row.get("level"),
        "kd": row.get("kd"),
        "assists": row.get("assists"),
        "kast": row.get("kast"),
        "kpr": row.get("kpr"),
        "fkfd": row.get("fkfd")
    }


def compact_state(state):
    latest = state.get("latest") or {}
    settings = ollama_settings()
    scoreboard = [
        compact_player(row, settings["player_name"])
        for row in latest.get("scoreboard", [])
    ]

    return {
        "game_id": state.get("current_game_id"),
        "my_player_name": settings["player_name"],
        "score": latest.get("score") or state.get("score"),
        "me": next((row for row in scoreboard if row.get("is_me")), None),
        "scoreboard": scoreboard,
        "live_duels": latest.get("live_duels", []),
        "recent_deaths": state.get("death_log", [])[-3:],
        "pipeline": {
            "mode": "full" if latest.get("full_scan") else "fast",
            "scan_ms": latest.get("scan_ms"),
            "cache_hits": latest.get("cache_hits"),
            "cache_misses": latest.get("cache_misses")
        }
    }


def choose_mention_target(context, audience):
    rows = context.get("scoreboard", [])

    if audience == "ally":
        candidates = [row for row in rows if row.get("team") == "allies" and row.get("name")]
    else:
        candidates = [row for row in rows if row.get("team") == "enemies" and row.get("name")]

    if not candidates or random.random() >= 0.33:
        return ""

    return random.choice(candidates).get("name", "")


def build_prompt(prompt_id, state):
    prompt = PROMPTS[prompt_id]
    context = compact_state(state)
    mention = choose_mention_target(context, prompt["audience"])
    settings = ollama_settings()

    prompt_text = (
        "You write exactly one Valorant chat message in French or franglais.\n"
        "Maximum one short sentence, 120 characters max. No explanation, no quotes, no markdown.\n"
        "Safety: no clear insults, slurs, threats, harassment, sexual content, self-harm, or ban-risk words.\n"
        "Never flame my own team. Enemy jabs may be annoying, smug, confusing or psychological, but stay safe.\n"
        "Ignore headshot percentage completely; HS% is not a useful signal for this assistant.\n"
        f"My in-game name is {settings['player_name']}. Treat that player as me even if OCR is weird.\n"
        "Use Valorant slang naturally if useful: btw, ez, smurf, diff, mental, clutch, trade, KAST, lurk, swing.\n"
        "You may use random philosophical or famous-sounding phrasing if it makes the lobby say WTF.\n"
        "Prefer real match facts over generic lines: score, my K-D, assists, KAST, live duels, recent deaths and who killed me.\n"
        f"Task: {prompt['instruction']}\n"
        f"Optional mention target, use only if it helps and keep the message safe: {mention}\n"
        f"Match context JSON: {json.dumps(context, ensure_ascii=False)}"
    )

    return {
        "model": settings["model"],
        "stream": False,
        "keep_alive": settings["keep_alive"],
        "options": {
            "temperature": settings["temperature"],
            "num_predict": 48
        },
        "prompt": prompt_text
    }


def sanitize_chat_line(text):
    line = re.sub(r"\s+", " ", (text or "").strip())
    line = line.split("\n", 1)[0].strip('"').strip("'").strip()

    banned_fragments = [
        "fuck", "shit", "bitch", "retard", "suicide", "kill yourself",
        "fdp", "pute", "ntm", "tg", "connard", "salope"
    ]
    lowered = line.lower()

    for fragment in banned_fragments:
        if fragment in lowered:
            return "btw your mental economy is on eco"

    if len(line) > 120:
        line = line[:117].rstrip() + "..."

    return line or "btw the spike has more philosophy than us"


def copy_to_clipboard(text):
    subprocess.run(
        ["powershell", "-NoProfile", "-Command", "Set-Clipboard -Value $input"],
        input=text,
        text=True,
        check=True
    )


def generate_message(prompt_id, state):
    if prompt_id == "disclaimer":
        copy_to_clipboard(FIXED_DISCLAIMER)
        return FIXED_DISCLAIMER, "fixed disclaimer"

    if prompt_id not in PROMPTS:
        raise ValueError(f"Unknown prompt id: {prompt_id}")

    settings = ollama_settings()
    payload = build_prompt(prompt_id, state)
    sent_prompt = payload["prompt"]
    data = json.dumps(payload).encode("utf-8")
    req = urllib.request.Request(
        f"{settings['url']}/api/generate",
        data=data,
        headers={"Content-Type": "application/json"},
        method="POST"
    )

    try:
        with urllib.request.urlopen(req, timeout=settings["timeout"]) as response:
            body = json.loads(response.read().decode("utf-8"))
    except urllib.error.URLError as exc:
        raise RuntimeError(f"Ollama unavailable: {exc}") from exc

    message = sanitize_chat_line(body.get("response", ""))
    copy_to_clipboard(message)
    return message, sent_prompt


